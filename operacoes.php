<?php

$soma = 2 + 2;
$subtracao = 2 - 2;
$multiplicacao = 2 * 2;
$divisao = 2 / 2;

$doisAoCubo = 2 ** 3; // o que seria 2 * 2 * 2

$restoDaDivisao = 10 % 3;

echo "A Soma é " .$soma;
echo "A Subtração é" .$subtracao;
echo "A Multiplicação é" .$multiplicacao;
echo "A Divisão é" .$divisao;

echo "O Resultado de Dois ao Cubo é" .$doisAoCubo;
echo "O resultado do resto da divisão é" .$restoDaDivisao;

